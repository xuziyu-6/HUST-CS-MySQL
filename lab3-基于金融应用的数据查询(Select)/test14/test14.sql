-- 14) 查询每份保险金额第4高保险产品的编号和保险金额。  
--     假设我们按保险金额从高到低排序，  
--     在数字序列8000,8000,7000,7000,6000中，  
--     两个8000均为第1高，两个7000均为第2高，6000为第3高，  
--     但我们要找的是第4高的金额（注意：在这个例子中实际上并没有第4高的金额，  
--     但为了说明查询方式，我们假设存在第4高的金额）。  
  
SELECT   
    i_id,  -- 保险产品的编号  
    i_amount  -- 保险金额  
FROM   
    insurance  -- 从保险表中选择数据  
WHERE   
    i_amount = (  
        -- 子查询用于找到第4高的保险金额  
        SELECT DISTINCT   
            i_amount  -- 选择不同的保险金额（尽管在这里不是必需的，但保持一致性）  
        FROM   
            insurance  -- 从保险表中选择数据  
        ORDER BY   
            i_amount DESC  -- 按保险金额降序排列  
        LIMIT 3, 1  -- LIMIT 3,1 表示跳过前3个记录，然后选择下一个记录，即第4高的金额  
    );  
  
